my_list=[10,20,'jessa',12.50,'emma',25,50]
#extracting a portion of the list from 2nd till 5th element 
print(my_list[2:5])

#slice first four items

print(my_list[:4])

#print every second element 
#with skip count 2

print(my_list[::2])

#reversing the list
print(my_list[::-1])

#without end value
# starting from 3rd item to last item

print(my_list[3:])